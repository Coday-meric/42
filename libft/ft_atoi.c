int	ft_atoi(char *str)
{
	int	res;
	int	nbm;
	int	i;

	i = 0;
	nbm = 0;
	res = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			nbm++;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		res = (res * 10) + (str[i] - 48);
		i++;
	}
	if (nbm % 2 == 1)
		res = res * -1;
	return (res);
}
